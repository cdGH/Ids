			byte[] buffer = new byte[DataPoolLength * 6];
			Array.Copy( iBuffer. GetBytes( ), 0, buffer, DataPoolLength * 0, DataPoolLength );
			Array.Copy( qBuffer. GetBytes( ), 0, buffer, DataPoolLength * 1, DataPoolLength );
			Array.Copy( mBuffer. GetBytes( ), 0, buffer, DataPoolLength * 2, DataPoolLength );
			Array.Copy( aiBuffer.GetBytes( ), 0, buffer, DataPoolLength * 3, DataPoolLength );
			Array.Copy( aqBuffer.GetBytes( ), 0, buffer, DataPoolLength * 4, DataPoolLength );
			Array.Copy( dBuffer. GetBytes( ), 0, buffer, DataPoolLength * 5, DataPoolLength );

			return buffer;
		}

