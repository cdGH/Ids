		private byte[] GetReadBackMessage( byte[] command, int select )
		{
			ushort address = BitConverter.ToUInt16( command, 44 );
			ushort length = BitConverter.ToUInt16( command, 46 );

			if (select == FanucHelper.SELECTOR_D)
				return FanucHelper.BuildReadResponseData( dBuffer.GetBytes( address * 2, length * 2 ) );
			else if (select == FanucHelper.SELECTOR_AI)
				return FanucHelper.BuildReadResponseData( aiBuffer.GetBytes( address * 2, length * 2 ) );
			else if (select == FanucHelper.SELECTOR_AQ)
				return FanucHelper.BuildReadResponseData( aqBuffer.GetBytes( address * 2, length * 2 ) );
			else if (select == FanucHelper.SELECTOR_M)
				return FanucHelper.BuildReadResponseData( SoftBasic.BoolArrayToByte( mBuffer.GetBytes( address, length ).Select( m => m != 0x00 ).ToArray( ) ) );
			else if (select == FanucHelper.SELECTOR_I)
				return FanucHelper.BuildReadResponseData( SoftBasic.BoolArrayToByte( iBuffer.GetBytes( address, length ).Select( m => m != 0x00 ).ToArray( ) ) );
			else if (select == FanucHelper.SELECTOR_Q)
				return FanucHelper.BuildReadResponseData( SoftBasic.BoolArrayToByte( qBuffer.GetBytes( address, length ).Select( m => m != 0x00 ).ToArray( ) ) );
			else
				return FanucHelper.BuildReadResponseData( new byte[0] );
		}

		private byte[] GetWriteBackMessage( byte[] command, int select )
		{
			// 先判断是否有写入的权利，没有的话，直接返回写入异常
			if (!EnableWrite) return null;

			ushort address = BitConverter.ToUInt16( command, command[2] == 8 ? 44 : 52 );
			ushort length = BitConverter.ToUInt16( command, command[2] == 8 ? 46 : 54 );
			int index = command[2] == 8 ? 48 : 56;
			if (select == FanucHelper.SELECTOR_D || select == FanucHelper.SELECTOR_AI || select == FanucHelper.SELECTOR_AQ)
			{
				if (select == FanucHelper.SELECTOR_D)
					dBuffer.SetBytes( SoftBasic.ArraySelectMiddle( command, index, length * 2 ), address * 2, length * 2 );
				else if (select == FanucHelper.SELECTOR_AI)
					aiBuffer.SetBytes( SoftBasic.ArraySelectMiddle( command, index, length * 2 ), address * 2, length * 2 );
				else if (select == FanucHelper.SELECTOR_AQ)
					aqBuffer.SetBytes( SoftBasic.ArraySelectMiddle( command, index, length * 2 ), address * 2, length * 2 );
			}
			else
			{
				int offset = address % 8;
				bool[] array = SoftBasic.ByteToBoolArray( SoftBasic.ArrayRemoveBegin( command, index ) );
				byte[] buffer = SoftBasic.ArraySelectMiddle( array, offset, length ).Select( m => m ? (byte)1 : (byte)0 ).ToArray( );

				if (select == FanucHelper.SELECTOR_M)
					mBuffer.SetBytes( buffer, address );
				else if (select == FanucHelper.SELECTOR_I)
					iBuffer.SetBytes( buffer, address );
				else if (select == FanucHelper.SELECTOR_Q)
					qBuffer.SetBytes( buffer, address );
			}

			byte[] back = SoftBasic.HexStringToBytes( @"03 00 09 00 00 00 00 00 00 01 00 00 00 00 00 00 00 01 00 00 00 00 00 00 
00 00 00 00 00 00 09 d4 10 0e 00 00 30 3a 00 00 01 01 00 00 00 00 00 00 01 01 ff 04 00 00 7c 21" );
			back[2] = command[2];
			return back;
		}

