		protected override void LoadFromBytes( byte[] content )
		{
			if (content.Length < DataPoolLength * 6) throw new Exception( "File is not correct" );

			iBuffer.SetBytes(  content, DataPoolLength * 0, 0, DataPoolLength );
			qBuffer.SetBytes(  content, DataPoolLength * 1, 0, DataPoolLength );
			mBuffer.SetBytes(  content, DataPoolLength * 2, 0, DataPoolLength );
			aiBuffer.SetBytes( content, DataPoolLength * 3, 0, DataPoolLength );
			aqBuffer.SetBytes( content, DataPoolLength * 4, 0, DataPoolLength );
			dBuffer.SetBytes(  content, DataPoolLength * 5, 0, DataPoolLength );
		}

